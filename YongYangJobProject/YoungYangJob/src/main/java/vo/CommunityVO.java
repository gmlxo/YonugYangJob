package vo;

public class CommunityVO {
	private String ity_idx;
	private String userid;
	private String ity_title;
	private String ity_contents;
	
	public CommunityVO() {
		super();
	}

	public String getIty_idx() {
		return ity_idx;
	}

	public void setIty_idx(String ity_idx) {
		this.ity_idx = ity_idx;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getIty_title() {
		return ity_title;
	}

	public void setIty_title(String ity_title) {
		this.ity_title = ity_title;
	}

	public String getIty_contents() {
		return ity_contents;
	}

	public void setIty_contents(String ity_contents) {
		this.ity_contents = ity_contents;
	}
}
